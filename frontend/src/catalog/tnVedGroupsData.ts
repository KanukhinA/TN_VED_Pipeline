const SECTION_VI_GROUP_MIN = "28";
const SECTION_VI_GROUP_MAX = "38";

function stripNotePrefix(s: string): string {
  return s
    .replace(/^Примечания:\s*/i, "")
    .replace(/^Примечание:\s*/i, "")
    .trim();
}

function inSectionVi(code: string): boolean {
  return code >= SECTION_VI_GROUP_MIN && code <= SECTION_VI_GROUP_MAX;
}

