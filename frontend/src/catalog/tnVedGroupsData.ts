export type TnVedGroupRef = {
  code: string;
  title: string;
  /** Текст примечаний для подсказки; пусто, если на источнике нет блока примечаний */
  description: string;
};

function describeGroup(code: string, title: string): string {
  const groupNotes = GROUP_NOTES_BY_CODE[code] ?? "";
  const groupUrl = `https://classinform.ru/tn-ved/gruppa-${code}.html`;
  if (!groupNotes) {
    return "";
  }
  const groupBody = stripNotePrefix(groupNotes);
  if (inSectionVi(code)) {
    const sectionBody = stripNotePrefix(SECTION_VI_NOTES);
    return [
      `Группа ${code}`,
      title,
      "",
      "Примечания к разделу VI:",
      sectionBody,
      "",
      `Примечания к группе ${code} (${groupUrl}):`,
      groupBody,
    ].join("\n");
  }
  return [`Группа ${code}`, title, "", `Примечания к группе ${code} (${groupUrl}):`, groupBody].join("\n");
}

/** 97 двузначных групп ТН ВЭД ЕАЭС (01–97); при нехватке строк в SHORT_TITLES подставляется заголовок-заглушка. */
export const TN_VED_GROUPS: TnVedGroupRef[] = Array.from({ length: 97 }, (_, i) => {
  const code = String(i + 1).padStart(2, "0");
  const title = SHORT_TITLES[i] ?? `Группа ${code}`;
  return { code, title, description: describeGroup(code, title) };
});

export function getTnVedGroup(code: string): TnVedGroupRef | undefined {
  const c = code.trim().padStart(2, "0");
  return TN_VED_GROUPS.find((g) => g.code === c);
}
